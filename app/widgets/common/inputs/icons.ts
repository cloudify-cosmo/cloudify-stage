const iconNameToIconClassName = (iconName: string) => iconName.replace(/[A-Z]/g, upperCaseLetter => `-${upperCaseLetter.toLowerCase()}`);

const iconClassName = (iconName: string) => {
    const iconClassName = iconNameToIconClassName(iconName);
    return `dds__icon dds__icon--${iconClassName}`;
};

export const iconButtonClassName = (iconName: string) => {
    const className = iconClassName(iconName);
    return `icon-button ${className}`;
}

export type Icons = Record<typeof iconNames[number], string>;

export const icons: Icons = iconNames.reduce(
    (iconsObj, iconName) => ({
        ...iconsObj,
        [iconName]: iconClassName(iconName)
    }),
    {} as Icons
);